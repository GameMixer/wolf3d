void	get_map(char mlxmap[MAP_W][MAP_H])
{
	int	x;
	int	y;

	x = 0;
	while (x < MAP_W)
	{
		y = 0;
		while (y < MAP_H)
		{
			if (x < 22)
				mlxmap[x][y] = map_part1(x, y);
			else if (x < 44)
				mlxmap[x][y] = map_part2(x, y);
			else
				mlxmap[x][y] = map_part3(x, y);
			y++;
		}
		x++;
	}
}
